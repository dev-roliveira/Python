"""
Tipo Booleano
Criado por George Boole

True / False

Negação - not
Fazendo a negação se o valor booleano for verdadeiro o resultado será falso e vice-versa.

Ou - or
Operação binária, depende de dois valores. Ou um ou outro deve ser verdadeiro

E - and
Operação binária, depende de dois valores. Ambos valores devem ser verdadeiro

"""
# Exemplo negação
ativo = False
print(ativo)
print(not ativo)

# Exemplo or
teste = True or False
