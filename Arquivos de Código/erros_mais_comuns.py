"""
Erros mais comuns
erro = exception em programação

"""

'SyntaxError'
# Erro de sintaxe, algo foi escrito que não é reconhecido como parte da linguagem Python.

'NameError'
# Erro quando uma função ou variável não foi definida.

'TypeError'
# Erro quando uma função/operação/ação é aplicada a um tipo errado

'IndexError'
# Erro de acesso de elemento em uma lista ou outro tipo de dado indexado, utilizando um indice invalido

'ValueError'
# Erro quando uma função/operação built-in recebe um argumento com tipo correto mas valor inapropriado

'KeyError'
# Erro que ocorre quando tentamos acessar um dicionário com uma chave inexistente

'AtributeError'
# Erro que ocorre quando uma variável não tem um atributo ou função

'IndentationError'
# Erro que ocorre quando não respeita-se a indentação do Python

