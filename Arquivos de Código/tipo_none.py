"""
O tipo None em python representa o tipo "sem tipo", ou poderia ser conhecido também como "tipo vazio", porém falar que
é um tipo sem tipo é mais comum

None - certo
none - errado

Quando utilizar?

Quando queremos criar uma variável e inicializa-la com um tipo sem tipo, antes de recebermos um valor.

"""

# Exemplo
numeros = None
print(numeros)
print(type(numeros))

numeros = 1.44, 1.55, 2.66
print(numeros)
print(type(numeros))

